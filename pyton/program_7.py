import statistics
a = [5, 8, 10, 15, 1, 9, 22]
print("Среднее значение:", statistics.mean(a))
print("Медиана:", statistics.median(a))
#Программа вывела среднее значение из представленных чисел, а также медиану 
#Модуль statistics подключается для высисления математической статистики и работы с вещественными числами