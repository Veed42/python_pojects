b = 'Hello world'
a = '.'.join(reversed(b))
print(a)
#программа вывела в обратном порядке символы разделяя буквы в них
# Метод join() отвечает за объединение списка строк с помощью какого-либо указетеля , в этом примере была использована точка  
#  Функция reversed() -  возвращает обратный итератор, то есть возвращает итератор, который перебирает элементы оригинала в обратном порядке возвращая символы в обратном порядке 
